function abrir(listaEjercicios){
    var listaEjercicios;
    listaEjercicios = document.getElementById(listaEjercicios);
    listaEjercicios.style = "display: block;"
}

function cerrar(listaEjercicios){
    var listaEjercicios;
    listaEjercicios = document.getElementById(listaEjercicios);
    listaEjercicios.style = "display: none;"
}